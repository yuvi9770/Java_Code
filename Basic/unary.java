public class unary {
    public static void main(String[] args) {
        // post incr
        int a = 6;
        System.out.println(a++);
        System.out.println(a);
        // pre incr
        int b = 9;
        System.out.println(++b);
        System.out.println(b);
        // post decr
        int w = 5;
        System.out.println(w--);
        System.out.println(w);
        // pre decr
        int q = 9;
        System.out.println(--q);
        System.out.println(q);

    }
}
