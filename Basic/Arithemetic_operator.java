public class Arithemetic_operator {
    public static void main(String[] args) {
        int a = 7;
        int b = 5;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
